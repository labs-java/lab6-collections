package com.bodkasoft.ingredient.vegetable.vegetabletypes;

import com.bodkasoft.ingredient.vegetable.Pepper;

/**
 * The {@code PepperType} enum represents various types of peppers.
 * Each constant in this enum corresponds to a specific variety of pepper,
 * which can vary in flavor, color, and heat level.
 *
 * <p>This enum can be used in conjunction with the {@link Pepper} class
 * to categorize and specify the type of pepper being represented.</p>
 *
 * <ul>
 * <li>{@link #SWEET} - A mild pepper variety with little to no heat.</li>
 * <li>{@link #HOT} - A pepper variety known for its spiciness and heat.</li>
 * <li>{@link #RED} - A variety of pepper that is typically mature and red in color.</li>
 * <li>{@link #GREEN} - An immature pepper that is harvested before ripening.</li>
 * <li>{@link #YELLOW} - A pepper variety that is ripe and yellow in color.</li>
 * <li>{@link #ORANGE} - A less common pepper variety that is ripe and orange.</li>
 * <li>{@link #PEPPERONI} - A type of pepper used mainly for flavoring in processed meats.</li>
 * <li>{@link #JALAPENO} - A medium-sized pepper known for its moderate heat.</li>
 * <li>{@link #SERRANO} - A small, spicy pepper that is hotter than a jalapeno.</li>
 * <li>{@link #CHILI} - A general term for various hot peppers.</li>
 * <li>{@link #BULGARIAN} - A variety of sweet pepper, often used in Bulgarian cuisine.</li>
 * </ul>
 *
 * @author YourName
 * @version 1.0
 */
public enum PepperType {
    SWEET,
    HOT,
    RED,
    GREEN,
    YELLOW,
    ORANGE,
    PEPPERONI,
    JALAPENO,
    SERRANO,
    CHILI,
    BULGARIAN
}
