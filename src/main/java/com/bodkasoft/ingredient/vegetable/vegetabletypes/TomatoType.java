package com.bodkasoft.ingredient.vegetable.vegetabletypes;

import com.bodkasoft.ingredient.vegetable.Tomato;

/**
 * The {@code TomatoType} enum represents various types of tomatoes.
 * Each constant in this enum corresponds to a specific variety of tomato.
 *
 * <p>This enum can be used in conjunction with the {@link Tomato} class
 * to categorize and specify the type of tomato being represented.</p>
 *
 * <ul>
 * <li>{@link #CHERRY} - A small, round, sweet variety of tomato.</li>
 * <li>{@link #PLUM} - An oval-shaped tomato, often used for sauces.</li>
 * <li>{@link #BEEFSTEAK} - A large, meaty tomato typically used for sandwiches.</li>
 * <li>{@link #COCKTAIL} - A bite-sized tomato variety, often used in salads.</li>
 * <li>{@link #GREEN} - Unripe tomatoes that are often used in specific dishes.</li>
 * <li>{@link #YELLOW} - A sweeter variety with a yellow skin.</li>
 * <li>{@link #PINK} - A type of tomato that has a pink hue, usually sweet.</li>
 * <li>{@link #BLACK} - A variety of tomato with dark, rich color, known for its unique flavor.</li>
 * <li>{@link #HYBRID} - A crossbreed of different tomato varieties for desired traits.</li>
 * <li>{@link #ORGANIC} - Tomatoes grown without synthetic fertilizers or pesticides.</li>
 * <li>{@link #ROMA} - A type of plum tomato, often used for making sauces.</li>
 * </ul>
 */
public enum TomatoType {
    CHERRY,
    PLUM,
    BEEFSTEAK,
    COCKTAIL,
    GREEN,
    YELLOW,
    PINK,
    BLACK,
    HYBRID,
    ORGANIC,
    ROMA
}
