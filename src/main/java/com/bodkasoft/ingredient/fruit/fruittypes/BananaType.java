package com.bodkasoft.ingredient.fruit.fruittypes;

import com.bodkasoft.ingredient.fruit.Banana;

/**
 * The {@code BananaType} enum represents various types of bananas.
 * Each constant in this enum corresponds to a specific variety of banana,
 * distinguished by its characteristics, flavor, and common uses.
 *
 * <p>This enum can be used in conjunction with the {@link Banana} class
 * to categorize and specify the type of banana being represented.</p>
 *
 * <ul>
 * <li>{@link #CAVENDISH} - The most common variety of banana, known for its sweetness and smooth texture.</li>
 * <li>{@link #RED} - A variety of banana with reddish-purple skin and a creamier texture, often sweeter than the Cavendish.</li>
 * <li>{@link #LAKATAN} - A sweet banana variety popular in Southeast Asia, known for its firm texture.</li>
 * <li>{@link #GIANT} - A larger variety of banana that is thicker and sweeter, often used in desserts.</li>
 * <li>{@link #PLANTAIN} - A starchy variety of banana that is typically cooked before eating; used in savory dishes.</li>
 * <li>{@link #PARADISE} - A less common variety, often found in tropical regions, known for its distinct flavor.</li>
 * <li>{@link #SUCRE} - A small, sweet banana variety often enjoyed fresh or in desserts.</li>
 * <li>{@link #THAI} - A type of banana known for its unique taste and aroma, commonly used in Thai cuisine.</li>
 * </ul>
 */
public enum BananaType {
    CAVENDISH,
    RED,
    LAKATAN,
    GIANT,
    PLANTAIN,
    PARADISE,
    SUCRE,
    THAI
}
